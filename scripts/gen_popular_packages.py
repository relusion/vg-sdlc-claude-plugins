"""gen_popular_packages.py — maintainer-run generator for dep-guard's typosquat corpus.

This is NOT a shipped gate. It lives in ``scripts/`` (outside the portability glob),
it USES THE NETWORK, and its OUTPUT — ``plugins/core-engineering/skills/ce-implement/
scripts/popular-packages.json`` plus the two forked copies — is committed, so the gate
(dep-guard.py) itself stays fully offline and deterministic. Rebuild the corpus, then
re-sync the forks:

    python3 scripts/gen_popular_packages.py --write
    python3 scripts/fork_sync.py --write

Ecosystem sources (top-N by download / popularity, fetched fresh each run; each source URL
and the fetch date are recorded in the output's ``_generated`` metadata key, which the
loader ignores):

  npm    wooorm/npm-high-impact ``lib/top-download.js`` — an array of package names in
         descending 30-day-download order (community-maintained, download-ranked).
  pypi   hugovk/top-pypi-packages ``top-pypi-packages.min.json`` — rows sorted by 30-day
         PyPI download count.
  nuget  NuGet's Azure Search query service — fetch the top 1000 candidates, then re-sort
         locally by ``totalDownloads`` (descending, id as a stable tie-break).
  cargo  crates.io API with ``sort=downloads`` — paginated 100 at a time.
  go     CURATED SEED. Go has no public download-ranked registry API: the module mirror
         (proxy.golang.org) publishes no download counts, and deps.dev exposes no
         top-N-by-popularity listing. Rather than fake a ranking, this list is a
         hand-maintained set of high-traffic Go modules — the genuine typosquat targets —
         defined in ``GO_SEED`` below and documented honestly. Extend it by hand.

Modes:
  --write        fetch every ecosystem and (re)write the canonical popular-packages.json.
                 This is the default when no mode flag is given.
  --check        fetch, then diff the ecosystem lists against the committed file; exit
                 0 (identical) / 1 (drift — re-run --write) / 2 (a fetch failed). An
                 idempotence dry-run — run it right after --write to confirm stability.
                 The volatile ``_generated`` date is intentionally excluded from the diff.
  --limit N      cap each ecosystem list at N names (default 500; the corpus stays a
                 bounded backstop, never a full registry mirror).

Stdlib only (urllib) — no third-party HTTP client, so a maintainer can regenerate from a
clean Python with nothing installed.
"""

