def _get_text(url: str) -> str:
    req = urllib.request.Request(url, headers={"User-Agent": UA})
    with urllib.request.urlopen(req, timeout=30) as resp:
        return resp.read().decode("utf-8")


def _get_json(url: str):
    return json.loads(_get_text(url))


def _dedup(names) -> list:
    """Preserve first-seen order; drop blanks and case-insensitive duplicates."""
    seen, out = set(), []
    for n in names:
        k = (n or "").strip()
        if k and k.lower() not in seen:
            seen.add(k.lower())
            out.append(k)
    return out


def fetch_npm(limit: int) -> list:
    text = _get_text(NPM_URL)
    # `export const topDownload = [ 'semver', 'minimatch', ... ]` — quoted names, in order.
    names = re.findall(r"'([^']+)'", text)
    if not names:
        raise RuntimeError("npm source returned no names (format changed?)")
    return _dedup(names)[:limit]


def fetch_pypi(limit: int) -> list:
    data = _get_json(PYPI_URL)
    rows = data.get("rows") or []
    if not rows:
        raise RuntimeError("pypi source returned no rows (format changed?)")
    return _dedup(r["project"] for r in rows)[:limit]


def fetch_nuget(limit: int) -> list:
    data = _get_json(NUGET_URL)
    rows = data.get("data") or []
    if not rows:
        raise RuntimeError("nuget source returned no data (format changed?)")
    rows.sort(key=lambda r: (-(r.get("totalDownloads") or 0), r.get("id", "").lower()))
    return _dedup(r["id"] for r in rows)[:limit]


def fetch_cargo(limit: int) -> list:
    out: list = []
    page = 1
    while len(out) < limit and page <= 20:
        data = _get_json(CRATES_URL.format(page=page))
        crates = data.get("crates") or []
        if not crates:
            break
        out.extend(c["name"] for c in crates)
        page += 1
    if not out:
        raise RuntimeError("cargo source returned no crates (format changed?)")
    return _dedup(out)[:limit]


def fetch_go(limit: int) -> list:
    return _dedup(GO_SEED)[:limit]


FETCHERS = {
    "npm": (fetch_npm, NPM_URL, "wooorm/npm-high-impact lib/top-download.js — 30-day downloads, descending"),
    "pypi": (fetch_pypi, PYPI_URL, "hugovk/top-pypi-packages — 30-day PyPI download count"),
    "nuget": (fetch_nuget, NUGET_URL, "NuGet Azure Search query service, re-sorted by totalDownloads desc"),
    "cargo": (fetch_cargo, CRATES_URL, "crates.io API, sort=downloads (paginated)"),
    "go": (fetch_go, "(none)", "curated seed — Go has no public download-ranked registry API; hand-maintained GO_SEED"),
}


def build(limit: int) -> dict:
    """Fetch every ecosystem and assemble the corpus dict. Raises on any fetch failure."""
    lists = {eco: FETCHERS[eco][0](limit) for eco in ECOSYSTEMS}
    out = {
        "_comment": REVISED_COMMENT,
        "_generated": {
            "by": "scripts/gen_popular_packages.py",
            "utc_date": datetime.now(timezone.utc).strftime("%Y-%m-%d"),
            "per_ecosystem_limit": limit,
            "counts": {eco: len(lists[eco]) for eco in ECOSYSTEMS},
            "sources": {eco: {"url": FETCHERS[eco][1], "note": FETCHERS[eco][2]} for eco in ECOSYSTEMS},
        },
    }
    for eco in ECOSYSTEMS:
        out[eco] = lists[eco]
    return out


def cmd_write(limit: int) -> int:
    try:
        corpus = build(limit)
    except Exception as e:  # noqa: BLE001 — a maintainer tool; report and exit non-zero
        print(f"gen-popular: fetch failed — {type(e).__name__}: {e}", file=sys.stderr)
        return 2
    TARGET.write_text(json.dumps(corpus, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    counts = corpus["_generated"]["counts"]
    print(f"gen-popular: wrote {TARGET.relative_to(REPO)} — " + ", ".join(f"{e}={counts[e]}" for e in ECOSYSTEMS))
    print("  next: python3 scripts/fork_sync.py --write   (re-sync the forked copies)")
    return 0


def cmd_check(limit: int) -> int:
    try:
        fresh = build(limit)
    except Exception as e:  # noqa: BLE001
        print(f"gen-popular: fetch failed — {type(e).__name__}: {e}", file=sys.stderr)
        return 2
    try:
        committed = json.loads(TARGET.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError) as e:
        print(f"gen-popular: cannot read committed corpus: {e}", file=sys.stderr)
        return 2
    drifted = [eco for eco in ECOSYSTEMS if fresh.get(eco) != committed.get(eco)]
    if drifted:
        print(f"gen-popular: DRIFT — {', '.join(drifted)} differ from a fresh fetch; re-run --write.")
        return 1
    print("gen-popular: OK — the committed corpus matches a fresh fetch (ecosystem lists identical).")
    return 0


def main(argv=None) -> int:
    p = argparse.ArgumentParser(description="Regenerate dep-guard's popular-packages.json typosquat corpus (network).")
    mode = p.add_mutually_exclusive_group()
    mode.add_argument("--write", action="store_true", help="fetch and (re)write the canonical corpus (default)")
    mode.add_argument("--check", action="store_true", help="fetch and diff against the committed corpus (0 same / 1 drift / 2 fetch fail)")
    p.add_argument("--limit", type=int, default=DEFAULT_LIMIT, help=f"per-ecosystem cap (default {DEFAULT_LIMIT})")
    args = p.parse_args(argv)
    limit = max(1, args.limit)
    if args.check:
        return cmd_check(limit)
    return cmd_write(limit)


if __name__ == "__main__":
    sys.exit(main())
